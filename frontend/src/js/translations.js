// --- دالة تطبيق اللغة (Logic) ---
let __gemaIsApplyingLanguage = false;
function setLanguage(lang) {
  if (__gemaIsApplyingLanguage) {
    return;
  }

  if (!lang) lang = "en";
  const nextLang = String(lang || 'en').trim().toLowerCase() || 'en';
  localStorage.setItem("selectedLang", nextLang);
  const previousLang = String(document.documentElement.lang || '').trim().toLowerCase();

  __gemaIsApplyingLanguage = true;

  try {
  const langDict = translations[nextLang] || {};
  const enDict = translations.en || {};

  // 1. ترجمة النصوص العادية
  const elements = document.querySelectorAll("[data-i18n]");
  elements.forEach((element) => {
    const key = element.getAttribute("data-i18n");
    const translatedValue = langDict[key] ?? enDict[key];
    if (translatedValue !== undefined) {
      element.innerHTML = translatedValue;
    }
  });

  // 2. ترجمة الـ Placeholders (مهم جداً للـ Forms)
  const placeholders = document.querySelectorAll("[data-i18n-placeholder]");
  placeholders.forEach((element) => {
    const key = element.getAttribute("data-i18n-placeholder");
    const translatedPlaceholder = langDict[key] ?? enDict[key];
    if (translatedPlaceholder !== undefined) {
      element.setAttribute("placeholder", translatedPlaceholder);
    }
  });

  // 3. ضبط الاتجاه واللغة الأساسية + كلاس اللغة على الـ body
  document.body.classList.remove('lang-ar', 'lang-en', 'lang-de', 'lang-zh', 'lang-tr');
  document.body.classList.add(`lang-${nextLang}`);

  document.dir = nextLang === "ar" ? "rtl" : "ltr";
  document.documentElement.lang = nextLang;

  // 4. المحرك السحري لملف الـ RTL (بيضيفه ويمسحه أوتوماتيك)
  const existingRtlLink = document.getElementById("rtl-style");
  if (nextLang === "ar") {
    if (!existingRtlLink) {
      const rtlLink = document.createElement("link");
      rtlLink.id = "rtl-style";
      rtlLink.rel = "stylesheet";
      rtlLink.href = "/css/rtl.css";
      // السطر ده بيرمي الملف في آخر الـ head عشان يغطي على الـ main.css
      document.head.appendChild(rtlLink);
    }
  } else {
    if (existingRtlLink) {
      existingRtlLink.remove(); // لو مش عربي، امسح الملف فوراً
    }
  }

  // 5. تحديث النص في زر اللغة
  const currentLangSpan = document.getElementById("current-lang");
  if (currentLangSpan) currentLangSpan.innerText = nextLang.toUpperCase();

  // 🔥 السطر السحري اللي ضفناه عشان يكلم صفحة الخدمات (وغيرها) ويخليها تتغير فوراً
  if (previousLang !== nextLang) {
    document.dispatchEvent(new CustomEvent('languageChanged', { detail: nextLang }));
  }
  } finally {
    __gemaIsApplyingLanguage = false;
  }
}

