function applyAdminHeaderTranslation(langCode) {
  const supported = ['ar', 'en', 'de', 'zh', 'tr'];
  const lang = supported.includes(langCode) ? langCode : 'en';
  const dict = ADMIN_HEADER_TRANSLATIONS[lang] || ADMIN_HEADER_TRANSLATIONS.en || {};

  document.querySelectorAll('[data-admin-i18n]').forEach((node) => {
    const key = node.getAttribute('data-admin-i18n');
    if (key && dict[key]) node.textContent = dict[key];
  });

  document.documentElement.setAttribute('lang', lang);
  document.body.classList.toggle('admin-lang-ar', lang === 'ar');
}

function setNodeText(selector, value) {
  const el = document.querySelector(selector);
  if (el && typeof value === 'string' && value.length) el.textContent = value;
}

function setNodeHtml(selector, value) {
  const el = document.querySelector(selector);
  if (el && typeof value === 'string' && value.length) el.innerHTML = value;
}

function setPlaceholder(selector, value) {
  const el = document.querySelector(selector);
  if (el && typeof value === 'string') el.setAttribute('placeholder', value);
}

function applyAdminDocumentMetadata(langCode) {
  const supported = ['ar', 'en', 'de', 'zh', 'tr'];
  const lang = supported.includes(langCode) ? langCode : 'en';
  const docDict = ADMIN_DOC_TRANSLATIONS[lang] || ADMIN_DOC_TRANSLATIONS.en || {};
  if (docDict.title) document.title = docDict.title;

  const descriptionMeta = document.querySelector('meta[name="description"]');
  if (descriptionMeta && docDict.description) descriptionMeta.setAttribute('content', docDict.description);
  const ogTitle = document.querySelector('meta[property="og:title"]');
  if (ogTitle && docDict.title) ogTitle.setAttribute('content', docDict.title);
  const ogDescription = document.querySelector('meta[property="og:description"]');
  if (ogDescription && docDict.description) ogDescription.setAttribute('content', docDict.description);
}

